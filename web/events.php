	<div class="event_text"><h3>Workshops with partner companies</h3>
		<p>These are our most frequent events. Connecting TUKE students with companies is one of our primary goals to help them with career prospects. We organise short events with partner companies in the form of workshops at TUKE, or Open Days in company offices. They usually include a part dedicated to hard-skills or soft-skills, such as “How to write a good CV” or “Coding in different programming languages”. Companies have a chance to also present themselves to the students, collect students’ CVs, and show them how work life in the company works.</p>
