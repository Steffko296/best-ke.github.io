	<div class="event_text">
		<h3>TechQuest</h3>
		<p>One day long event for TUKE students in Jedlíkova Student Dormitories, where partner companies prepare different short tasks for students to complete, such as finding bugs in code, quizzes, and logical tasks. By completing all tasks, students are able to enter a raffle competition with prizes. The first year of TechQUEST connected more than 100 students to different companies and student organisations.</p>
