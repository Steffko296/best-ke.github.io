	<div class="event_text">
		<h3>BEST Course</h3>
		<p>10-14 day long event for 20-25 TUKE students and students from technical universities from all 85 Local BEST Groups from all over Europe. It consists of an academic part led by TUKE lecturers or partner company representatives focused on a specific topic (such as programming in Java language, 3D printing, Internet of Things…) where participants learn and present their results at the end of the course. During the non-academic part of a BEST Course, we show participants Kosice and Slovak culture, monuments, historical places, and traditions.</p>
	</div>
	
